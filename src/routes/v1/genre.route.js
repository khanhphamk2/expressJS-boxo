const express = require('express');
const auth = require('../../middlewares/auth');
const validate = require('../../middlewares/validate');
const genreValidation = require('../../validations/genre.validation');
const genreController = require('../../controllers/genre.controller');
const { roles } = require('../../config/role.enum');

const router = express.Router();

router
  .route('/')
  .post(auth(roles.ADMIN, roles.MANAGER), validate(genreValidation.createGenre), genreController.createGenre)
  .get(validate(genreValidation.getGenres), genreController.getGenres);

router
  .route('/:genreId')
  .get(validate(genreValidation.getGenre), genreController.getGenre)
  .put(auth(), validate(genreValidation.updateGenre), genreController.updateGenre)
  .delete(auth(), validate(genreValidation.deleteGenre), genreController.deleteGenre);

module.exports = router;

