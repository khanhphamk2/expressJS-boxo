const express = require('express');
const auth = require('../../middlewares/auth');
const validate = require('../../middlewares/validate');
const discountValidation = require('../../validations/discount.validation');
const discountController = require('../../controllers/discount.controller');
const { roles } = require('../../config/role.enum');

const router = express.Router();

router.route('/public').get(discountController.getPublicDiscounts);

router.use(auth(roles.ADMIN));

router
  .route('/')
  .post(validate(discountValidation.createDiscount), discountController.createDiscount)
  .get(validate(discountValidation.getDiscounts), discountController.getDiscounts);

router
  .route('/:discountId')
  .get(validate(discountValidation.getDiscount), discountController.getDiscount)
  .put(validate(discountValidation.updateDiscount), discountController.updateDiscount)
  .delete(validate(discountValidation.deleteDiscount), discountController.deleteDiscount);

module.exports = router;

